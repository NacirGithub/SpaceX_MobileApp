package com.example.spacex.model;

public class CompanyLinksModel {
    private String website, flickr, twitter, elon_twitter;

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getFlickr() {
        return flickr;
    }

    public void setFlicker(String flicker) {
        this.flickr = flicker;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String twitter) {
        this.twitter = twitter;
    }

    public String getElon_twitter() {
        return elon_twitter;
    }

    public void setElon_twitter(String elon_twitter) {
        this.elon_twitter = elon_twitter;
    }
}
